package uy.edu.um.prog2.FileToObjects;

public class Clase {
	private String nombre;
	private int idClase;

	public Clase(int idClase, String nombre) {
		this.nombre = nombre;
		this.idClase = idClase;
	}

	public String getNombre() {
		return nombre;
	}


	public int getIdClase() {
		return idClase;
	}

	
	
}
